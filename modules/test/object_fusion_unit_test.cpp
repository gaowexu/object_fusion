BEVDetClassificationType GenerateBEVDetClassificationProb(const std::string category_name, const float confidence)
{

    BEVDetClassificationType classification;
    std::fill(classification.begin(), classification.end(), 0.0F);

    std::map<std::string, BEVDetClassification> lut;
    lut["BUS"] = BEVDetClassification::kBus;
    lut["CAR"] = BEVDetClassification::kCar;
    lut["CYCLIST"] = BEVDetClassification::kBicycle;
    lut["OTHER_VEHICLE"] = BEVDetClassification::kOtherVehicle;
    lut["PEDESTRIAN"] = BEVDetClassification::kPedestrian;
    lut["SMALL_MANPOWER_VEHICLE"] = BEVDetClassification::kMicroTricycle;
    lut["TEMPORARY_ROAD_BLOCK"] = BEVDetClassification::kOtherRoadblock;
    lut["TRAILER"] = BEVDetClassification::kTrailer;
    lut["TRUCK"] = BEVDetClassification::kTruck;

    classification[static_cast<size_t>(lut[category_name])] = confidence;
    return classification;
}

